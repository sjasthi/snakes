def build_letter_index(word_list):
    index = {}
    for word in word_list:
        for i, ch in enumerate(word.upper()):
            if ch not in index:
                index[ch] = []
            index[ch].append({
                "clue_word": word,
                "letter": ch,
                "position": i + 1,
                "length": len(word),
                "hint": f"{i + 1}/{len(word)}"
            })
    return index

